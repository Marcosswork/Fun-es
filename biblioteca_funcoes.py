def somarNumeros(n1, n2):
    res = n1 + n2
    return res
    
def subtrairNumeros(n1, n2): 
    res = n1 - n2
    return res

def multiplicarNumeros(n1, n2):
    res = n1 * n2
    return res

def dividirNumeros(n1, n2):   
    res = n1 / n2
    return res